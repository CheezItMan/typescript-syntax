var add = function (num1, num2) {
    return num1 + num2;
};
var subtract = function (num1, num2) {
    return num1 - num2;
};
var divide = function (num1, num2) {
    return num1 / num2;
};
var multiply = function (a, b) {
    return a * b;
};
console.log(add(5, 6));
console.log(subtract(5, 6));
console.log(divide(55, 6));
console.log(multiply(5, 6));
